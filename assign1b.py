# Python program to greet the user with their full name

# Taking first name and last name as input
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")

# Concatenating into full name
full_name = first_name + " " + last_name

# Printing a personalized greeting
print(f"\nHello, {full_name}! Welcome to the program.")
